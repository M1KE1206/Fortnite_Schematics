// Eenmalig draaien: node scripts/generate-schematic-names.mjs
import { mkdirSync, writeFileSync } from 'node:fs';

const SITEMAP = 'https://stw-planner.com/sitemap.xml';
const RARITY = new Set(['c', 'uc', 'r', 'vr', 'sr']);

const xml = await (await fetch(SITEMAP)).text();
const re = /schematics\/([a-z0-9-]+)\/([A-Za-z0-9_]+)/g;
const map = {};
let skipped = 0;
let m;
while ((m = re.exec(xml)) !== null) {
  const [, nameSlug, sid] = m;
  let sidTokens = sid.toLowerCase().split('_');
  if (sidTokens[0] === 'trap') sidTokens = sidTokens.slice(1);
  const rarityToken = RARITY.has(sidTokens[sidTokens.length - 1]) ? sidTokens[sidTokens.length - 1] : null;
  const sidTokensWithoutRarity = rarityToken ? sidTokens.slice(0, -1) : sidTokens;
  const baseKey = sidTokensWithoutRarity.join('_');
  const exactKey = rarityToken ? `${baseKey}_${rarityToken}` : baseKey;

  const nameTokens = nameSlug.split('-');
  if (RARITY.has(nameTokens[0])) nameTokens.shift();
  const name = nameTokens.map((w) => (w ? w[0].toUpperCase() + w.slice(1) : w)).join(' ');

  // Write base key only if it doesn't exist yet (first-wins for base keys)
  if (!(baseKey in map)) {
    map[baseKey] = name;
  } else if (map[baseKey] !== name) {
    skipped++;
  }

  // Write exact key only if it's different from base key (unconditional when different)
  if (exactKey !== baseKey) {
    map[exactKey] = name;
  }
}

const sorted = Object.fromEntries(Object.entries(map).sort(([a], [b]) => a.localeCompare(b)));
const body = `// Generated by scripts/generate-schematic-names.mjs - do not edit by hand.\nexport const SCHEMATIC_NAMES: Record<string, string> = ${JSON.stringify(sorted, null, 2)};\n`;
mkdirSync('server/data', { recursive: true });
writeFileSync('server/data/schematicNames.ts', body);
console.log(`Wrote ${Object.keys(sorted).length} names (${skipped} conflicting duplicates skipped).`);
